-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Hensa = {}
Tunnel.bindInterface("cards", Hensa)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CARDS:OPEN
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("cards:Open")
AddEventHandler("cards:Open", function()
	SetNuiFocus(true, true)
	TriggerEvent("Notify", "default", "Raspe o espaço inteiro para resgatar o prêmio.", false, 10000)
	SendNUIMessage({ action = "open", minPrice = 1, maxPrice = 100, tryAgainPercentage = 0.7 })
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- EXIT
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("exit", function()
	SetNuiFocus(false, false)
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- GIVEMONEY
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("giveMoney", function(data)
	local Value = tonumber(data.price)
	TriggerServerEvent("cards:Payment", Value)
end)
local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Hensa = {}
Tunnel.bindInterface("cards", Hensa)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CARDS:PAYMENT
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterServerEvent("cards:Payment")
AddEventHandler("cards:Payment", function(Price)
	local source = source
	local Passport = vRP.Passport(source)
	if Passport and Price then
		vRP.GiveBank(Passport, Price)
		TriggerClientEvent("Notify", source, "verde", "VocÃª ganhou <b>$"..Price.." "..ItemName(DefaultDollars1).."</b>.", "Sucesso", 5000)
	end
end)